package com.codingapi.report.render.grid;

/**
 * 扩展方向：数据在格子上的铺开方式。这是类 Excel 报表的<b>本质机制</b>。
 *
 * <h3>为什么扩展方向是核心？</h3>
 * <p>报表里没有刚性的"表"对象，整个报表的逻辑结构由每个格子的
 * "扩展方向 + 父格"组合涌现出来。扩展方向决定了数据怎么在格子上铺开：
 *
 * <h3>三种方向</h3>
 * <ul>
 *   <li>{@link #VERTICAL}（纵向扩展 / 行关系）：一条记录占一行，数据向下铺开。
 *       最常见的模式——员工列表、销售明细等都是纵向扩展。
 *       <pre>
 *         姓名 │ 部门 │ 工资     ← 模板行（CellBinding × 3）
 *         张三 │ 研发 │ 8000     ← 第 1 条记录
 *         李四 │ 测试 │ 7000     ← 第 2 条记录
 *       </pre></li>
 *
 *   <li>{@link #HORIZONTAL}（横向扩展 / 列关系）：一条记录占一列，数据向右铺开。
 *       典型用于动态列——月份列、指标列等列数不固定的场景。
 *       <pre>
 *         部门 │ 1月   │ 2月   │ 3月     ← "月份"字段横向扩展
 *         研发 │ 50000 │ 48000 │ 52000   ← 交叉格的聚合值
 *       </pre></li>
 *
 *   <li>{@link #NONE}（不扩展）：格子不铺开，通常配合
 *       聚合表达式（如 {@code COUNT}）取单值。
 *       <pre>
 *         员工总数: 42              ← COUNT(员工.id), expansion=NONE
 *       </pre></li>
 * </ul>
 *
 * <h3>交叉表（矩阵报表）</h3>
 * <p>VERTICAL + HORIZONTAL 正交组合即交叉表：一个字段纵向扩展当行维度，
 * 一个字段横向扩展当列维度，交叉格放聚合值。
 * <pre>
 *              1月(SUM)  2月(SUM)  3月(SUM)    ← HORIZONTAL 扩展
 *   研发(VERT)  50000     48000     52000       ← 交叉格 = SUM(工资)
 *   测试(VERT)  30000     32000     28000
 * </pre>
 */
public enum Expansion {
    /** 纵向扩展（行关系）：一条记录占一行，向下铺开 */
    VERTICAL,
    /** 横向扩展（列关系）：一条记录占一列，向右铺开 */
    HORIZONTAL,
    /** 不扩展：取单值，通常配合聚合 */
    NONE
}
